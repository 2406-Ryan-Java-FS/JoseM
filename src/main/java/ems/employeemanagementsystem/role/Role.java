package ems.employeemanagementsystem.role;

/**
 * Enumeration representing the roles that can be assigned to an account.
 * There are two roles: ROLE_USER and ROLE_ADMIN.
 */
public enum Role {
    ROLE_USER,  // Standard user role with limited permissions.
    ROLE_ADMIN  // Admin role with elevated permissions, such as viewing all employees.


}
